%% ¿Qué es LABPaint?
% LABPaint es una aplicación desarrollada en *MATLAB* con fines didácticos,
% se pretende mostrar el uso de los eventos generados por el mouse en una
% GUI MATLAB y otras características muy útiles como los menús, menús 
% contextuales, el selector de color, entre otras herramientas. 
%

%% Menú Archivo
%
% * *Guardar:* Guarda el dibujo actual en formato PNG.
% * *Salir:* Cierra la aplicación.

%% Menú Herramientas
%
% * *Lápiz:* Crea un dibujo utilizando un lápiz (Mano alzada)
% * *Círculo:* Dibuja un círculo, partiendo del centro y arrastrando hasta
% lograr el radio requerido.
% * *Rectángulo:* Dibuja un rectángulo a partir de una de sus esquinas y
% arrastrando hasta la esquina opuesta.
% * *Polilínea:* Dibuja lineas continuas, para indicar la terminación de
% una linea utilice el clic izquierdo, para terminar de dibujar una
% polilínea utilice el clic derecho.
% * *Aerógrafo:* Simula un aerógrafo mientras mantiene presionado y
% arrastra el mouse.
% * *Borrar linea:* Borra una línea seleccionada.
% * *Borrar lienzo:* Borra todo el lienzo de dibujo.

%% Menú Color
%
% * *Seleccionar color:* Modifica el color utilizado por las herramientas
% de dibujo.
% * *Color del lienzo:* Modifica el color del lienzo.

%% Menú Grosor
% Permite seleccionar un grosor de línea para las herramientas de dibujo.

